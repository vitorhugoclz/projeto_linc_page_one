                            Resumo: Descrição: Os sistemas elétricos de potência tem se tornado cada vez mais complexos e na sua operação várias variáveis são consideradas. Uma grande preocupação é operar o sistema de forma otimizada minimizando o custo total e os riscos associados. Nesta linha de raciocínio, a previsão de carga é fundamental pois o despacho econômico, fluxo de potência, análise de estabilidade, entre outras ações importantes na operação de um sistema de potência dependem da previsão de carga Neste sentido, este projeto objetiva realizar um estudo investigativo em duas técnicas baseadas árvores de regressão, o Random Forest e o XGBoost, visando avaliar a sua aplicabilidade em problemas de previsão de carga elétrica no curto prazo. Nas simulações propostas pretende-se utilizar os dados de carga de domínio público disponibilizados pela competição EUNITE para medir o desempenho das técnicas no problema proposto.
