                            Resumo: A energia elétrica ocupa um lugar de destaque no país, gerando grandes quantidades de dados que ficam sujeitos a falhas dos equipamentos, erros de medição, dados incompletos, erros humanos, entre outros. Assim, análise dos dados se torna uma tarefa crítica, principalmente em ambientes que exijam segurança e confiabilidade dos dados, uma vez que a presença de dados inconsistente prejudica na tomada de decisão, além de afetar o desempenho do sistema, a segurança e a confiabilidade das informações. Em muitos problemas do setor elétrico, como na previsão de carga ou na previsão por barramento, a qualidade dos dados reflete diretamente no resultado obtido. O processo KDD, por sua vez, tem um papel importante por estar apto a lidar com grandes volumes de dados, que, sem a ajuda da inteligência computacional, seriam inviáveis se fossem investigados por pessoas, por maior que fosse a equipe de trabalho. Assim, neste trabalho é proposto um modelo para identificação e tratamento de outliers, utilizando o processo KDD, dando ênfase aos métodos estatísticos para identificação de outliers, e aos operadores média e redes neurais artificiais (RNA) para o devido tratamento. Neste modelo, o tratamento dos dados de demanda de carga, de certo dia, baseia-se nas cargas elétricas dos dias próximos, já que as cargas elétricas apresentam comportamento similar em dias semelhantes como dias úteis, sábados, domingos e feriados. Porém, certos eventos, como feriados, devem ser analisados com atenção especial para não serem tratados como outliers, já que apresentam grande variação de comportamento em relação a um dia útil, por exemplo. Para a execução dos experimentos deste trabalho, serão utilizados dados de medição obtidos do sistema elétricos brasileiros compreendidos no período de 01/01/2010 a 31/12/2010, com a medição efetuada com discretização por minutos.</p>
