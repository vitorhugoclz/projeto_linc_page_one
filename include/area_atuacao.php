                            <h5>Introdução</h5>
                            <p>Nowadays, many numerical optimization problems have a great diffi-
                                culty to be resolved, both in the field of continuous solutions and discrete solutions. It
                                occurs due to the fact that the amount of possible solutions to a problem may be enormous
                                on the search space, being, generally, an exponential amount in relation to the instance
                                of the problem (nondeterministic polynomial time – NP) [1].
                                So often, the search for the best solution for a particular problem will not be realistic for
                                the time available and an approximate result is perhaps the best choice at the time. On
                                this feature, researchers from around the world offer new methods to solve optimization
                                problems more simply and/or efficiently [2]. A simple proof of this is the new intelligence-
                                based optimization techniques of swarms, where through cooperation between individuals
                                (direct or indirect) can obtain a better adaptation of population to the environment.
                                An example of an intelligent swarm technique that will be addressed in this paper
                                is the Ant Colony Optimization (ACO), inspired by the behavior of agents (artificial
                                ants) in search of food.  The ACO is especially recommended for work in the field of
                                discrete solutions for numerical optimization problems.  The ACO can be applied in
                                various issues, being among the best known, for example, the traveling salesman problem,
                                such as generating timetables and vehicle routing, which are difficult computing problems.
                                With this, the ACO has been showing as a promising approach and competitive in relation
                                to other strategies presented in the literature.</p>
