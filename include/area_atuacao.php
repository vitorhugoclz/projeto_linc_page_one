                            <h5>Introducao</h5>
                            <p>Qualidade dos dados no setor elétrico é de extrema
                                importância,  uma  vez  que  estes  dados  contêm
                                informações  que  refletem  o  estado  operacional  do  sistema.
                                Contudo, dentre os elementos que compõem este conjunto de
                                dados,  há  um  tipo  especial  de  elemento  que  se  deve  ter  a
                                preocupação de se avaliar, o
                                outlier .
                                O outlier é um elemento de uma série de dados que, por algum  motivo,  se  distancia
                                de  forma  característica  à normalidade  da  amostra  em  questão,  parecendo  assim  ser
                                inconsistentes  de  acordo  com  o  restante  desse  conjunto  de
                                dados  [1].  Anscombe  [2]  define  um
                                outlier como  uma observação com grandes anomalias residuais.
                                Outliers são  geralmente  comuns  em  base  de  dados,  no
                                entanto,  sua  verdadeira  causa  de  ocorrência  é  geralmente
                                desconhecida  para  os  analistas  de  dados  [3].  Algumas  das
                                causas que podem ser atribuídas aos seus aparecimentos numa
                                amostra são principalmente, anomalias de medição inerentes a
                                falhas nos sensores, erros inerentes à execução, ou ainda a
                                fatores aleatórios. Mesmo com ferramentas visuais altamente
                                sofisticadas, na maioria dos casos se torna inviável analisar os
                                dados utilizando processos identif icação manual [4], [6]. Fato
                                que  se  atribui  a  grande  quantidade  de  informações  e  alta
                                dimensionalidade dos dados.</p>
