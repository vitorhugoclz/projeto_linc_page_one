                            <h5>Introdução: </h5>
                            <p>The flow of a gas–liquid mixture in a pipe is distinguished by
                                the spatial distribution of the interfaces which separate the phases.
                                In some flows the interfaces may evolve smoothly along the pipe
                                while in other flows they assume complex shapes such as ripples,
                                dendrites, dimples and irregular shapes that resemble fractals. The
                                interfaces’ shape and area density depend on the flow rates, fluid
                                properties and pipe’s size and orientation. By their turn, the inter-
                                faces with different shapes and area densities result in flows with
                                distinct friction, heat and mass transfer coefficients. The coupling
                                between the interface’s shape and the interface’s transport coeffi-
                                cients is one of the challenging aspects in two phase flow.
                                The development of transport equations to the interface’s geo-
                                metrical properties are not developed yet. In an attempt to over-
                                come this deficiency a simpler approach is adopted by grouping
                                the interfaces accordingly to their topological similarities and asso-
                                ciating each group to a specific interfacial transfer mechanism to
                                flow modeling</p>
