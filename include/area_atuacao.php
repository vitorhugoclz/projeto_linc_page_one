                            <h5>Resumo:</h5>
                            <p>ste trabalho apresenta um mecanismo que possibilita a redução das chances do processo de otimização de funções não lineares estacionarem em mínimos locais, ao utilizarem a meta-heurística Otimização por Enxame de Partículas (PSO). Tal mecanismo é uma forma não monotônica de controlar a inércia da  partícula,  que  é  um  dos  fatores  responsáveis  pela  movimentação  desta  durante  o  processo  de otimização. Os resultados experimentais foram comparados com o modelo original da PSO padrão, a  fim  de  mostrar  o  potencial  para  encontrar  uma  melhor  solução  em  funções  de  benchmark,  em problemas complexos. Ao final, uma comparação de ambos os modelos aplicados a um problema real para a regulagem de pesos sinápticos de uma rede neural do tipo Multi-Layer Perceptron foi feita, e as PSOs, por se tratarem de técnicas de propósito geral, mostraram resultados interessantes.</p>
