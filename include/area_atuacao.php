                            <h5>Intrudução: </h5>
                            <p>The   electrical   energy   is   produced   at   power   plants   and transported to the consumption centers through transmission systems.  A  transmission  system is  composed  mainly  by transmission lines, transformers and control devices and can be  mathematically  modeled  as  graph  flow  system,  in  which the arcs represent transmission lines and transformers and the nodes,  called  bus,  represent  the  connection  point  of  these components.    The    electrical    energy    consumptions    are connected  to  these  buses  and  the  sum  of  all  consumers  of  a bus gives its bus load demand to be supplied. The electrical energy  company  must  operate  th e  system  safely  and  in  this sense it is important to assess the operation of the transmission system, that is based on load forecast on each bus [1]. The   assessment   of   transmission   system   operation   is executed at the short-term operational planning step, in which operation  schedule  of  the  power  system  for  the  next  day  is determined. This short-term planning must specify in hourly or  half-hour  basis  the  start-up  and  shut-down  of  generation units and the dispatch of these online units. This solution must satisfy   all   power   generation   and   transmission   systems requirements.  To  assure  transmission  secure  operation,  it  is important  to  evaluate  the  impact of  the  dispatch  at  a  given time  interval,  considering  the  transmission  network  and  bus load   expected   to   that   interv al   [2].</p>
