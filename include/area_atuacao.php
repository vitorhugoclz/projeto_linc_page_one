                            <p>O mercado  de  ações  é  visto  como  uma  fonte  de  renda variável   de   natureza   instável   e   complexa,   que   é influenciada   por   inúmeros   fatores   sejam   estes,   políticos, econômicos,  sociológicos  ou  psicológicos.  O  conhecimento aproximado  em  relação  ao  valor  futuro  de  um  determinado ativo  é  um  dado  essencial  para  que  os  investidores  possam realizar  o  processo  de  tomada  de  decisão  no  mercado,  isto  é vender e comprar ativos de forma a obter lucro nas transações. Para auxiliar o investidor, existem atualmente dois tipos de estratégias   para   análise   de   dados   em   ativos:   a   análise fundamentalista e  a técnica.  A  estratégia  fundamentalista  se baseia  no  contexto  socioeconômico,  no  qual  as  informações são obtidas das mais variadas fontes a fim de tentar determinar o  valor  das  ações  de  um  determinado  ativo  no  mercado.  Por outro  lado,  a  análise  técnica  utiliza  com  base  em  informações históricas  das  ações  (valores  numéricos)  e  via  modelagem matemática faz a previsão do valor dos ativos. Ambas  as  estratégias  consistem  na  análise  de  grandes conjuntos  de  dados  e  a  utilização  de  técnicas  computacionais pode  auxiliar  na  análise  dessas  informações,  assim  como identificar momentos adequados para as negociações.  </p>
